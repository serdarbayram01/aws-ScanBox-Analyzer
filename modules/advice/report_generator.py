def _t(key, lang='tr'):
    return _L.get(key, {}).get(lang, key)


def _txt(item, lang='tr', key_en='text_en', key_tr='text_tr'):
    if lang == 'tr':
        return item.get(key_tr) or item.get(key_en) or ''
    return item.get(key_en) or ''


def _cat(svc, lang='tr'):
    # Service/category names always in English for consistency
    return svc.get('category_en') or svc.get('service', '')


# WAFR code → AWS doc URL
_WAFR_DOC_BASE = {
    'SEC':  'https://docs.aws.amazon.com/wellarchitected/latest/security-pillar/welcome.html',
    'OPS':  'https://docs.aws.amazon.com/wellarchitected/latest/operational-excellence-pillar/welcome.html',
    'REL':  'https://docs.aws.amazon.com/wellarchitected/latest/reliability-pillar/welcome.html',
    'PERF': 'https://docs.aws.amazon.com/wellarchitected/latest/performance-efficiency-pillar/welcome.html',
    'COST': 'https://docs.aws.amazon.com/wellarchitected/latest/cost-optimization-pillar/welcome.html',
    'SUS':  'https://docs.aws.amazon.com/wellarchitected/latest/sustainability-pillar/welcome.html',
}


def _wafr_url(code):
    m = _re.match(r'^([A-Z]+)', code)
    pillar = m.group(1) if m else ''
    return _WAFR_DOC_BASE.get(pillar, '#')


def _wafr_link(code):
    url = _wafr_url(code)
    return f'<a class="wafr-code" href="{url}" target="_blank" rel="noopener" title="{code}">{code}</a>'


