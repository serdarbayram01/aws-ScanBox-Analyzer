def _build_service_nav(services, lang):
    """Build nav links for one language."""
    prefix = 'en' if lang == 'en' else 'tr'
    lines = []
    for idx, svc in enumerate(services, 1):
        cat = _cat(svc, lang)
        svc_id = f'svc-{prefix}-{idx}'
        fc = len(svc.get('findings', []))
        pc = len(svc.get('positive', []))
        bcls = 'has-findings' if fc else ('all-pass' if pc else '')
        bval = fc if fc else (f'\u2713{pc}' if pc else '0')
        lines.append(
            f'<a class="nav-link" href="#{svc_id}">'
            f'<span style="flex:1;overflow:hidden;text-overflow:ellipsis;white-space:nowrap">{idx}. {cat}</span>'
            f'<span class="count-badge {bcls}">{bval}</span></a>'
        )
    return '\n'.join(lines)


def _build_body(results, lang):
    """Build the main body HTML for one language."""
    profile = results.get('profile', 'unknown')
    account_id = results.get('account_id', '')
    assess_time = results.get('assessment_time', '')
    summary = results.get('summary', {})
    services = results.get('services', [])
    pillar_scores = results.get('pillar_scores', {})
    risk = summary.get('risk_counts', {})

    h = ''
    # Meta
    h += f'''<table class="meta-table">
      <tr><td class="label">{_t('profile',lang)}:</td><td class="value">{profile}</td></tr>
      <tr><td class="label">{_t('account',lang)}:</td><td class="value" style="color:#c2410c">{account_id}</td></tr>
      <tr><td class="label">{_t('date',lang)}:</td><td class="value">{assess_time}</td></tr>
    </table>'''

    # Summary
    h += f'<h2 class="section-title" id="summary">{_t("summary",lang)}</h2>'
    h += f'''<div class="summary-grid">
      <div class="summary-card"><div class="val val-high">{risk.get("HIGH",0)}</div><div class="lbl">{_t("high_risk",lang)}</div></div>
      <div class="summary-card"><div class="val val-medium">{risk.get("MEDIUM",0)}</div><div class="lbl">{_t("medium_risk",lang)}</div></div>
      <div class="summary-card"><div class="val val-low">{risk.get("LOW",0)}</div><div class="lbl">{_t("low_risk",lang)}</div></div>
      <div class="summary-card"><div class="val val-pos">{summary.get("total_positive",0)}</div><div class="lbl">{_t("positive",lang)}</div></div>
      <div class="summary-card"><div class="val val-svc">{summary.get("services_analyzed",0)}</div><div class="lbl">{_t("services",lang)}</div></div>
    </div>'''

    # Pillar scores
    h += f'<h2 class="section-title" id="pillars">{_t("pillar_scores",lang)}</h2>'
    h += '<table class="pillar-table"><thead><tr>'
    h += f'<th>{_t("pillar",lang)}</th><th>{_t("score",lang)}</th><th>{_t("findings",lang)}</th><th>{_t("positive",lang)}</th></tr></thead><tbody>'
    for code, info in pillar_scores.items():
        pn = info.get('name_tr','') if lang=='tr' else info.get('name_en',code)
        sc = info.get('score',0)
        bc = '#dc2626' if sc<30 else '#d97706' if sc<60 else '#16a34a'
        h += f'<tr><td style="font-weight:600">{pn}</td>'
        h += f'<td style="font-weight:700;color:{bc}">{sc}% <span class="pillar-bar"><span class="pillar-bar-fill" style="width:{sc}%;background:{bc}"></span></span></td>'
        h += f'<td>{info.get("findings",0)}</td><td>{info.get("positive",0)}</td></tr>'
    h += '</tbody></table>'

    # Service sections
    prefix = 'en' if lang == 'en' else 'tr'
    for idx, svc in enumerate(services, 1):
        cat = _cat(svc, lang)
        findings = svc.get('findings', [])
        recs = svc.get('recommendations', [])
        positive = svc.get('positive', [])

        h += f'<h2 class="section-title" id="svc-{prefix}-{idx}">{idx}. {cat}</h2>'

        if findings:
            h += f'<h3 class="sub-heading finding">{_t("tespitler",lang)}: ({len(findings)})</h3>'
            h += '<ul class="findings-list">'
            for f in findings:
                rv = f.get('risk','LOW')
                txt = _txt(f, lang)
                codes = ' '.join(_wafr_link(c) for c in f.get('wafr_codes',[]))
                meta = []
                if f.get('region'): meta.append(f['region'])
                if f.get('resource_id'): meta.append(f['resource_id'])
                ms = f' <span style="color:var(--text-meta);font-size:9pt">({", ".join(meta)})</span>' if meta else ''
                lc = rv.lower() if rv in ('MEDIUM','LOW') else ''
                h += f'<li class="{lc}"><span class="risk-badge risk-{rv}">{rv}</span>{codes}{ms}<br>{txt}</li>'
            h += '</ul>'

        if recs:
            h += f'<h3 class="sub-heading rec">{_t("oneriler",lang)}: ({len(recs)})</h3>'
            h += '<ul class="rec-list">'
            for r in recs:
                txt = _txt(r, lang)
                codes = ' '.join(_wafr_link(c) for c in r.get('wafr_codes',[]))
                h += f'<li>{txt} {codes}</li>'
            h += '</ul>'

        if positive:
            h += f'<h3 class="sub-heading pos">{_t("olumlu",lang)}: ({len(positive)})</h3>'
            h += '<ul class="pos-list">'
            for p in positive:
                h += f'<li>&#10003; {_txt(p, lang)}</li>'
            h += '</ul>'

    h += f'<div class="footer">{_t("generated_by",lang)} &mdash; {assess_time}</div>'
    return h


