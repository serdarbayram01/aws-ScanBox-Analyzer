# PDF Report — Professional A4 Portrait (ReportLab)
# ═══════════════════════════════════════════════════════════════════════════════

def generate_pdf(results: dict, ts: str = None, lang: str = 'tr') -> str:
    """Generate a single PDF in the specified language."""
    ts = ts or _ts()
    profile = results.get('profile', 'unknown')
    services = results.get('services', [])
    summary = results.get('summary', {})
    pillar_scores = results.get('pillar_scores', {})
    risk = summary.get('risk_counts', {})

    filename = f'advice_{profile}_{ts}_{lang}.pdf'
    filepath = os.path.join(REPORTS_DIR, filename)

    try:
        from reportlab.lib.pagesizes import A4
        from reportlab.lib import colors
        from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
        from reportlab.lib.enums import TA_CENTER
        from reportlab.platypus import (
            SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle,
            HRFlowable,
        )
        from reportlab.lib.units import mm
        from reportlab.pdfbase import pdfmetrics
        from reportlab.pdfbase.ttfonts import TTFont

        # Register Unicode-capable fonts for Turkish characters (ş,ç,ğ,ı,ö,ü,İ,Ş,Ç,Ğ)
        _font_paths = {
            'Arial':     ['/System/Library/Fonts/Supplemental/Arial.ttf',
                          '/Library/Fonts/Arial Unicode.ttf',
                          '/usr/share/fonts/truetype/dejavu/DejaVuSans.ttf'],
            'ArialBold': ['/System/Library/Fonts/Supplemental/Arial Bold.ttf',
                          '/Library/Fonts/Arial Unicode.ttf',
                          '/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf'],
        }
        _font_registered = False
        for fname, paths in _font_paths.items():
            for p in paths:
                if os.path.isfile(p):
                    try:
                        pdfmetrics.registerFont(TTFont(fname, p))
                        _font_registered = True
                        break
                    except Exception:
                        continue

        _fn = 'Arial' if _font_registered else 'Helvetica'
        _fnb = 'ArialBold' if _font_registered else 'Helvetica-Bold'

        doc = SimpleDocTemplate(
            filepath, pagesize=A4,
            topMargin=20*mm, bottomMargin=20*mm,
            leftMargin=18*mm, rightMargin=18*mm,
        )

        styles = getSampleStyleSheet()
        s_title = ParagraphStyle('RTitle', parent=styles['Title'],
                                 fontName=_fnb, fontSize=18,
                                 textColor=colors.HexColor('#0f1b2d'), spaceAfter=4)
        s_h2 = ParagraphStyle('RH2', parent=styles['Heading2'],
                               fontName=_fnb, fontSize=14,
                               textColor=colors.HexColor('#0f1b2d'),
                               spaceBefore=16, spaceAfter=8)
        s_h3f = ParagraphStyle('RH3F', parent=styles['Heading3'],
                                fontName=_fnb, fontSize=11,
                                textColor=colors.HexColor('#b91c1c'),
                                spaceBefore=10, spaceAfter=4)
        s_h3r = ParagraphStyle('RH3R', parent=styles['Heading3'],
                                fontName=_fnb, fontSize=11,
                                textColor=colors.HexColor('#047857'),
                                spaceBefore=10, spaceAfter=4)
        s_h3p = ParagraphStyle('RH3P', parent=styles['Heading3'],
                                fontName=_fnb, fontSize=11,
                                textColor=colors.HexColor('#15803d'),
                                spaceBefore=10, spaceAfter=4)
        s_bullet = ParagraphStyle('RBullet', parent=styles['Normal'],
                                   fontName=_fn, fontSize=9, leading=13,
                                   leftIndent=12, bulletIndent=0,
                                   spaceBefore=3, spaceAfter=3)
        s_footer = ParagraphStyle('RFooter', parent=styles['Normal'],
                                   fontName=_fn, fontSize=7, leading=9,
                                   textColor=colors.HexColor('#94a3b8'),
                                   alignment=TA_CENTER)

        elements = []

        # Title
        elements.append(Paragraph(_t('report_title', lang), s_title))
        elements.append(HRFlowable(width='100%', thickness=2,
                                    color=colors.HexColor('#ff9900'), spaceAfter=8))

        # Meta
        meta_data = [
            [_t('profile',lang)+':', profile],
            [_t('account',lang)+':', results.get('account_id','')],
            [_t('date',lang)+':', results.get('assessment_time','')],
        ]
        mt = Table(meta_data, colWidths=[110, None])
        mt.setStyle(TableStyle([
            ('FONTSIZE',(0,0),(-1,-1),9),
            ('TEXTCOLOR',(0,0),(0,-1),colors.HexColor('#64748b')),
            ('FONTNAME',(0,0),(0,-1),_fnb),
            ('FONTNAME',(1,0),(1,-1),_fnb),
            ('VALIGN',(0,0),(-1,-1),'TOP'),
            ('BOTTOMPADDING',(0,0),(-1,-1),2),
        ]))
        elements.append(mt)
        elements.append(Spacer(1, 10*mm))

        # Summary
        elements.append(Paragraph(_t('summary',lang), s_h2))
        sd = [[
            f"{_t('high_risk',lang)}\n{risk.get('HIGH',0)}",
            f"{_t('medium_risk',lang)}\n{risk.get('MEDIUM',0)}",
            f"{_t('low_risk',lang)}\n{risk.get('LOW',0)}",
            f"{_t('positive',lang)}\n{summary.get('total_positive',0)}",
            f"{_t('services',lang)}\n{summary.get('services_analyzed',0)}",
        ]]
        st = Table(sd, colWidths=[None]*5)
        st.setStyle(TableStyle([
            ('ALIGN',(0,0),(-1,-1),'CENTER'), ('FONTSIZE',(0,0),(-1,-1),9),
            ('FONTNAME',(0,0),(-1,-1),_fn),
            ('BOX',(0,0),(-1,-1),0.5,colors.HexColor('#e2e8f0')),
            ('INNERGRID',(0,0),(-1,-1),0.5,colors.HexColor('#e2e8f0')),
            ('BACKGROUND',(0,0),(-1,-1),colors.HexColor('#f8fafc')),
            ('TOPPADDING',(0,0),(-1,-1),8), ('BOTTOMPADDING',(0,0),(-1,-1),8),
        ]))
        elements.append(st)
        elements.append(Spacer(1, 6*mm))

        # Pillar scores
        elements.append(Paragraph(_t('pillar_scores',lang), s_h2))
        pd = [[_t('pillar',lang), _t('score',lang), _t('findings',lang), _t('positive',lang)]]
        for code, info in pillar_scores.items():
            pn = info.get('name_tr','') if lang=='tr' else info.get('name_en',code)
            pd.append([pn, f"{info.get('score',0)}%", str(info.get('findings',0)), str(info.get('positive',0))])
        pt = Table(pd, colWidths=[None, 60, 60, 60])
        pt.setStyle(TableStyle([
            ('BACKGROUND',(0,0),(-1,0),colors.HexColor('#1e3a5f')),
            ('TEXTCOLOR',(0,0),(-1,0),colors.white),
            ('FONTNAME',(0,0),(-1,0),_fnb),
            ('FONTNAME',(0,1),(-1,-1),_fn),
            ('FONTSIZE',(0,0),(-1,-1),9),
            ('GRID',(0,0),(-1,-1),0.5,colors.HexColor('#e2e8f0')),
            ('ROWBACKGROUNDS',(0,1),(-1,-1),[colors.white,colors.HexColor('#f8fafc')]),
            ('TOPPADDING',(0,0),(-1,-1),6), ('BOTTOMPADDING',(0,0),(-1,-1),6),
        ]))
        elements.append(pt)
        elements.append(Spacer(1, 8*mm))

        # Service sections
        for idx, svc in enumerate(services, 1):
            cat = _cat(svc, lang)
            findings = svc.get('findings',[])
            recs = svc.get('recommendations',[])
            positive = svc.get('positive',[])

            elements.append(Paragraph(f'{idx}. {cat}', s_h2))

            if findings:
                elements.append(Paragraph(f'<b>{_t("tespitler",lang)} ({len(findings)})</b>', s_h3f))
                for f in findings:
                    rv = f.get('risk','LOW')
                    txt = _txt(f, lang)
                    codes = ', '.join(f.get('wafr_codes',[]))
                    meta = []
                    if f.get('region'): meta.append(f['region'])
                    if f.get('resource_id'): meta.append(f['resource_id'])
                    ms = f' ({", ".join(meta)})' if meta else ''
                    bt = f'<b>[{rv}]</b> {txt}{"  ("+codes+")" if codes else ""}{ms}'
                    elements.append(Paragraph(f'<bullet>&bull;</bullet>{bt}', s_bullet))

            if recs:
                elements.append(Paragraph(f'<b>{_t("oneriler",lang)} ({len(recs)})</b>', s_h3r))
                for r in recs:
                    txt = _txt(r, lang)
                    codes = ', '.join(r.get('wafr_codes',[]))
                    bt = f'{txt}{"  ("+codes+")" if codes else ""}'
                    elements.append(Paragraph(f'<bullet>&bull;</bullet>{bt}', s_bullet))

            if positive:
                elements.append(Paragraph(f'<b>{_t("olumlu",lang)} ({len(positive)})</b>', s_h3p))
                for p in positive[:10]:
                    elements.append(Paragraph(f'<bullet>&#10003;</bullet>{_txt(p,lang)}', s_bullet))

            elements.append(Spacer(1, 4*mm))

        # Footer
        elements.append(Spacer(1, 10*mm))
        elements.append(HRFlowable(width='100%', thickness=0.5, color=colors.HexColor('#e2e8f0')))
        elements.append(Spacer(1, 3*mm))
        elements.append(Paragraph(
            f'{_t("generated_by",lang)} &mdash; {results.get("assessment_time","")}', s_footer))

        doc.build(elements)

    except ImportError:
        with open(filepath, 'w', encoding='utf-8') as fh:
            fh.write(f'PDF generation requires reportlab. Profile: {profile}')

    return filename


# ═══════════════════════════════════════════════════════════════════════════════
# Generate All — HTML (bilingual) + PDF-TR + PDF-EN  (shared timestamp)
# ═══════════════════════════════════════════════════════════════════════════════

def generate_all(results: dict, **kwargs) -> dict:
    """Generate HTML (bilingual) + PDF-TR + PDF-EN with shared timestamp."""
    ts = _ts()
    return {
        'html':   generate_html(results, ts),
        'pdf_tr': generate_pdf(results, ts, 'tr'),
        'pdf_en': generate_pdf(results, ts, 'en'),
    }
