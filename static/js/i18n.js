// Default: use OS preference if no saved preference exists
let _theme = localStorage.getItem('finops_theme') ||
  (window.matchMedia && window.matchMedia('(prefers-color-scheme: light)').matches ? 'light' : 'dark');

function setTheme(theme) {
  _theme = theme;
  localStorage.setItem('finops_theme', theme);
  document.documentElement.setAttribute('data-theme', theme === 'light' ? 'light' : '');
  document.querySelectorAll('.theme-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.theme === theme);
  });
  document.dispatchEvent(new CustomEvent('themechange', { detail: { theme } }));
}

function getTheme() { return _theme; }

// Listen for OS theme changes (only if user hasn't manually chosen)
if (window.matchMedia) {
  window.matchMedia('(prefers-color-scheme: light)').addEventListener('change', (e) => {
    if (!localStorage.getItem('finops_theme')) {
      setTheme(e.matches ? 'light' : 'dark');
    }
  });
}

