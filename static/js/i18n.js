let _lang = localStorage.getItem('finops_lang') || 'en';

function t(key) {
  return TRANSLATIONS[_lang][key] || TRANSLATIONS['en'][key] || key;
}

function setLang(lang) {
  _lang = lang;
  localStorage.setItem('finops_lang', lang);
  applyTranslations();

  // Update toggle buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Dispatch event so charts/tables can re-render labels
  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

function getLang() { return _lang; }

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (el.placeholder !== undefined && el.tagName !== 'SELECT') {
      el.placeholder = t(key);
    } else {
      el.textContent = t(key);
    }
  });
  // HTML translations (for strings containing inline <code>, <strong> etc.)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    const val = t(key);
    if (val) el.innerHTML = val;
  });
}

